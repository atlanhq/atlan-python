#!/usr/bin/env python3
"""Generate __init__.py and __init__.pyi for the v9 assets package.

Scans all _init_*.py files in this directory to build:
1. __init__.py — lazy_loader.attach() with module→class mapping
2. __init__.pyi — explicit imports for IDE type hints

Run after syncing Pkl-generated files:
    uv run python pyatlan_v9/model/assets/_generate_pkg_init.py
"""

from __future__ import annotations

import ast
import pkgutil
from pathlib import Path

ASSETS_DIR = Path(__file__).parent

# Base classes that are always eagerly imported (not lazy-loaded)
EAGER_IMPORTS = [
    ("entity", ["AtlasClassification", "Entity", "TermAssignment"]),
    ("related_entity", ["RelatedEntity", "SaveSemantic"]),
]


def _scan_init_modules() -> tuple[dict[str, list[str]], dict[str, str]]:
    """Scan _init_*.py files and return (lazy_mapping, class_to_source).

    lazy_mapping: {_init_module_name: [class_names]} for lazy_loader
    class_to_source: {class_name: source_module_name} for .pyi imports
    """
    lazy_mapping: dict[str, list[str]] = {}
    class_to_source: dict[str, str] = {}

    for module_info in sorted(
        pkgutil.iter_modules([str(ASSETS_DIR)]),
        key=lambda m: m.name,
    ):
        if not module_info.name.startswith("_init_"):
            continue

        module_file = ASSETS_DIR / f"{module_info.name}.py"
        try:
            tree = ast.parse(module_file.read_bytes())
        except SyntaxError:
            continue

        # Extract __all__ list
        all_names: list[str] = []
        for node in ast.walk(tree):
            if isinstance(node, ast.Assign):
                for target in node.targets:
                    if isinstance(target, ast.Name) and target.id == "__all__":
                        if isinstance(node.value, (ast.List, ast.Tuple)):
                            for elt in node.value.elts:
                                if isinstance(elt, ast.Constant) and isinstance(
                                    elt.value, str
                                ):
                                    all_names.append(elt.value)

        if all_names:
            lazy_mapping[module_info.name] = all_names

        # Extract from .X import Y statements for .pyi
        for node in ast.walk(tree):
            if isinstance(node, ast.ImportFrom) and node.module and node.level == 1:
                source_module = node.module
                for alias in node.names:
                    name = alias.asname if alias.asname else alias.name
                    if name in all_names:
                        class_to_source[name] = source_module

    return lazy_mapping, class_to_source


def _generate_init_py(lazy_mapping: dict[str, list[str]]) -> str:
    """Generate __init__.py content with lazy_loader."""
    lines = [
        "# Auto-generated by _generate_pkg_init.py — DO NOT EDIT",
        "# SPDX-License-Identifier: Apache-2.0",
        "# Copyright 2024 Atlan Pte. Ltd.",
        "# isort: skip_file",
        "",
        "import lazy_loader as lazy",
        "",
    ]

    # Eager imports
    for module, names in EAGER_IMPORTS:
        lines.append(f"from .{module} import {', '.join(names)}")
    lines.append("")

    # Lazy mapping dict
    lines.append("__PYATLAN_V9_ASSETS__ = {")
    for mod_name, class_names in sorted(lazy_mapping.items()):
        if len(class_names) <= 3:
            names_str = ", ".join(f'"{n}"' for n in class_names)
            lines.append(f'    "{mod_name}": [{names_str}],')
        else:
            lines.append(f'    "{mod_name}": [')
            for name in class_names:
                lines.append(f'        "{name}",')
            lines.append("    ],")
    lines.append("}")
    lines.append("")

    # lazy_loader.attach
    lines.append(
        "__getattr__, __dir__, __all__ = lazy.attach("
        "__name__, submod_attrs=__PYATLAN_V9_ASSETS__"
        ")"
    )
    lines.append("")

    # Add eager imports to __all__
    eager_names = [n for _, names in EAGER_IMPORTS for n in names]
    eager_str = ", ".join(f'"{n}"' for n in eager_names)
    lines.append(f"__all__ += [{eager_str}]")
    lines.append("")

    return "\n".join(lines)


def _generate_init_pyi(
    lazy_mapping: dict[str, list[str]],
    class_to_source: dict[str, str],
) -> str:
    """Generate __init__.pyi content with explicit imports."""
    lines = [
        "# Auto-generated by _generate_pkg_init.py — DO NOT EDIT",
        "# SPDX-License-Identifier: Apache-2.0",
        "# Copyright 2024 Atlan Pte. Ltd.",
        "",
    ]

    # Collect all names for __all__
    all_names: list[str] = []
    for _, names in EAGER_IMPORTS:
        all_names.extend(names)
    for _, class_names in sorted(lazy_mapping.items()):
        all_names.extend(class_names)

    # __all__
    lines.append("__all__ = [")
    for name in all_names:
        lines.append(f'    "{name}",')
    lines.append("]")
    lines.append("")

    # Eager base class imports
    for module, names in EAGER_IMPORTS:
        for name in names:
            lines.append(f"from .{module} import {name} as {name}")
    lines.append("")

    # Lazy class imports (from their source modules)
    for _, class_names in sorted(lazy_mapping.items()):
        for name in class_names:
            source = class_to_source.get(name)
            if source:
                lines.append(f"from .{source} import {name} as {name}")
            else:
                # Fallback: couldn't determine source module
                lines.append(f"# WARNING: could not resolve source module for {name}")
    lines.append("")

    return "\n".join(lines)


def main():
    lazy_mapping, class_to_source = _scan_init_modules()

    init_py = _generate_init_py(lazy_mapping)
    init_pyi = _generate_init_pyi(lazy_mapping, class_to_source)

    init_path = ASSETS_DIR / "__init__.py"
    pyi_path = ASSETS_DIR / "__init__.pyi"

    init_path.write_text(init_py)
    pyi_path.write_text(init_pyi)

    # Summary
    total_classes = sum(len(v) for v in lazy_mapping.values())
    eager_count = sum(len(names) for _, names in EAGER_IMPORTS)
    print(
        f"Generated __init__.py: {len(lazy_mapping)} modules, {total_classes} lazy classes"
    )
    print(f"Generated __init__.pyi: {total_classes + eager_count} type stubs")


if __name__ == "__main__":
    main()
